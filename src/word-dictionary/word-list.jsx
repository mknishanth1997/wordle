export function give5LetterWord() {
  let arrayIndex = Math.floor(Math.random() * 212);
  let result = fiveLetterWords[arrayIndex];
  let formattedResult = [...result].map((letter, index) => ({
    letter: letter,
    position: index + 1,
  }));
  return [result, formattedResult];
}
